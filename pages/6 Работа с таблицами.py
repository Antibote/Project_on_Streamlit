# –§–∏–ª—å—Ç—Ä–∞—Ü–∏—è –ø–æ —Ç–µ–º–ø–µ—Ä–∞—Ç—É—Ä–µ
temp_filter = st.slider("–ü–æ–∫–∞–∑–∞—Ç—å —Ç–æ–ª—å–∫–æ —Å—Ç—Ä–æ–∫–∏ —Å —Ç–µ–º–ø–µ—Ä–∞—Ç—É—Ä–æ–π –≤—ã—à–µ:", 15, 35, 20)
filtered = data[data["–¢–µ–º–ø–µ—Ä–∞—Ç—É—Ä–∞, ¬∞C"] > temp_filter]

st.write(f"–†–µ–∑—É–ª—å—Ç–∞—Ç —Ñ–∏–ª—å—Ç—Ä–∞—Ü–∏–∏ (T > {temp_filter} ¬∞C):")
st.dataframe(filtered)

st.success("–¢–∞–±–ª–∏—Ü—ã –º–æ–∂–Ω–æ —Ñ–∏–ª—å—Ç—Ä–æ–≤–∞—Ç—å, –∏–∑–º–µ–Ω—è—Ç—å –∏ –≤–∏–∑—É–∞–ª–∏–∑–∏—Ä–æ–≤–∞—Ç—å –ø—Ä—è–º–æ –≤ –±—Ä–∞—É–∑–µ—Ä–µ.")
