import numpy as np
st.page_link("./app.py", label="–í–µ—Ä–Ω—É—Ç—å—Å—è –Ω–∞ –≥–ª–∞–≤–Ω—É—é")
st.title("üìä –†–∞–±–æ—Ç–∞ —Å —Ç–∞–±–ª–∏—Ü–∞–º–∏")

st.markdown("–ü—Ä–∏–º–µ—Ä –æ—Ç–æ–±—Ä–∞–∂–µ–Ω–∏—è –∏ –æ–±—Ä–∞–±–æ—Ç–∫–∏ –¥–∞–Ω–Ω—ã—Ö –≤ —Ç–∞–±–ª–∏—Ü–∞—Ö.")

# –°–æ–∑–¥–∞–µ–º DataFrame
data = pd.DataFrame({
    "–¢–µ–º–ø–µ—Ä–∞—Ç—É—Ä–∞, ¬∞C": np.random.randint(15, 35, 10),
    "–í–ª–∞–∂–Ω–æ—Å—Ç—å, %": np.random.randint(30, 90, 10),
    "–°–∫–æ—Ä–æ—Å—Ç—å –≤–µ—Ç—Ä–∞, –º/—Å": np.random.uniform(0.5, 5.0, 10).round(2)
})

st.write("–ò—Å—Ö–æ–¥–Ω—ã–µ –¥–∞–Ω–Ω—ã–µ:")
